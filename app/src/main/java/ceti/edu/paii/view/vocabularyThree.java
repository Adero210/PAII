package ceti.edu.paii.view;

public class vocabularyThree {

    String PreguntaUno;
    String respuestaUno;
    String PreguntaDos;
    String respuestaDos;
    String PreguntaTres;
    String respuestaTres;
    String PreguntaCuatro;
    String respuestaCuatro;
    String PreguntaCinco;
    String respuestaCinco;

    public vocabularyThree(String preguntaUno, String respuestaUno, String preguntaDos, String respuestaDos, String preguntaTres, String respuestaTres, String preguntaCuatro, String respuestaCuatro, String preguntaCinco, String respuestaCinco) {
        PreguntaUno = preguntaUno;
        this.respuestaUno = respuestaUno;
        PreguntaDos = preguntaDos;
        this.respuestaDos = respuestaDos;
        PreguntaTres = preguntaTres;
        this.respuestaTres = respuestaTres;
        PreguntaCuatro = preguntaCuatro;
        this.respuestaCuatro = respuestaCuatro;
        PreguntaCinco = preguntaCinco;
        this.respuestaCinco = respuestaCinco;
    }


    public String getPreguntaUno() {
        return PreguntaUno;
    }

    public void setPreguntaUno(String preguntaUno) {
        PreguntaUno = preguntaUno;
    }

    public String getRespuestaUno() {
        return respuestaUno;
    }

    public void setRespuestaUno(String respuestaUno) {
        this.respuestaUno = respuestaUno;
    }

    public String getPreguntaDos() {
        return PreguntaDos;
    }

    public void setPreguntaDos(String preguntaDos) {
        PreguntaDos = preguntaDos;
    }

    public String getRespuestaDos() {
        return respuestaDos;
    }

    public void setRespuestaDos(String respuestaDos) {
        this.respuestaDos = respuestaDos;
    }

    public String getPreguntaTres() {
        return PreguntaTres;
    }

    public void setPreguntaTres(String preguntaTres) {
        PreguntaTres = preguntaTres;
    }

    public String getRespuestaTres() {
        return respuestaTres;
    }

    public void setRespuestaTres(String respuestaTres) {
        this.respuestaTres = respuestaTres;
    }

    public String getPreguntaCuatro() {
        return PreguntaCuatro;
    }

    public void setPreguntaCuatro(String preguntaCuatro) {
        PreguntaCuatro = preguntaCuatro;
    }

    public String getRespuestaCuatro() {
        return respuestaCuatro;
    }

    public void setRespuestaCuatro(String respuestaCuatro) {
        this.respuestaCuatro = respuestaCuatro;
    }

    public String getPreguntaCinco() {
        return PreguntaCinco;
    }

    public void setPreguntaCinco(String preguntaCinco) {
        PreguntaCinco = preguntaCinco;
    }

    public String getRespuestaCinco() {
        return respuestaCinco;
    }

    public void setRespuestaCinco(String respuestaCinco) {
        this.respuestaCinco = respuestaCinco;
    }
}
